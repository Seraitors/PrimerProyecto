package EstructurasDeControl;

public class EjemplosWhile {
    public static void main(String[] args) {

        // vamos a hacer un bucle while que imprima los numeros del 0 al 4
        int i=0;

        while (i <5){//mientras i se menor que 5 true enttramos en el bucle
            //dentro del bucle imprimimos
            System.out.println(i);
            // es importante que dentro del while haya alguna cosa que haga que el bucle pare en tdo momet
            //normalmente es una avriable que cambia de valor
            //hasta que toma un valor que la condicion sea false
            i++;


        }


    }
}
